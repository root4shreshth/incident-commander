"""Export training trajectories as a HuggingFace-ready dataset.

This is the proof-of-concept artifact for the data-factory thesis: a published
dataset of SRE incident-response trajectories generated by the simulator.
Two formats are produced:

    results/hf_dataset/raw.jsonl
        One JSON object per (episode, step) — full observation, action,
        and 6-component reward breakdown. The substrate format other
        researchers can train any policy on.

    results/hf_dataset/sft.jsonl
        Chat-style {system, user, assistant} rows, ready to drop into
        TRL's SFTTrainer. Mixes (a) generated random-policy episodes and
        (b) the senior-SRE behavioral-clone IDEAL_TRAJECTORIES.

    results/hf_dataset/manifest.json
        Counts + provenance + how-to-load snippet.

Usage::

    uv run python scripts/export_trajectories.py
    uv run python scripts/export_trajectories.py --seeds 10
    uv run python scripts/export_trajectories.py --push-to-hub <user>/<repo>

Reuses (no edits):
- evaluate(), random_policy() — training/eval_runner.py
- read_episode(), iter_runs() — training/episode_logger.py
- SYSTEM_PROMPT, build_sft_dataset() — training/datasets.py
- IDEAL_TRAJECTORIES — incident_commander_env/server/coach.py
"""

from __future__ import annotations

import argparse
import json
import shutil
import sys
import time
from pathlib import Path
from typing import Any, Dict, Iterable, List, Optional

REPO_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO_ROOT))

from training.datasets import SYSTEM_PROMPT, build_sft_dataset
from training.episode_logger import iter_runs, read_episode
from training.eval_runner import evaluate, random_policy

FAMILIES = ("oom_crash", "db_pool_exhaustion", "bad_deployment_cascade")
DEFAULT_RUNS_DIR = REPO_ROOT / "results" / "dataset_runs"
DEFAULT_OUTPUT_DIR = REPO_ROOT / "results" / "hf_dataset"


# ----------------------------------------------------------------------------
# Step 1: generate raw episodes by running the random policy across seeds
# ----------------------------------------------------------------------------


def generate_episodes(seeds_per_family: int, runs_dir: Path) -> Dict[str, Any]:
    """Run `random_policy` across all 3 families × N seeds, writing JSONL traces."""
    runs_dir.mkdir(parents=True, exist_ok=True)

    print(f"[generate] {seeds_per_family} seeds per family × {len(FAMILIES)} families "
          f"= {seeds_per_family * len(FAMILIES)} episodes", flush=True)
    print(f"[generate] writing to {runs_dir}", flush=True)

    t0 = time.perf_counter()
    report = evaluate(
        condition_name="random",
        act=random_policy(rng_seed=42),
        families=list(FAMILIES),
        seeds=list(range(seeds_per_family)),
        system_prompt=SYSTEM_PROMPT,
        difficulty=0.5,
        runs_root=str(runs_dir),
    )
    elapsed = time.perf_counter() - t0

    print(f"[generate] done in {elapsed:.2f} s "
          f"({report.n_episodes} episodes, {report.n_episodes / elapsed:.1f} ep/s)", flush=True)
    return {
        "elapsed_seconds": elapsed,
        "n_episodes": report.n_episodes,
        "by_family": report.by_family,
    }


# ----------------------------------------------------------------------------
# Step 2: convert each episode into "raw" rows (one row per step)
# ----------------------------------------------------------------------------


def _strip_observation(obs: Dict[str, Any]) -> Dict[str, Any]:
    """Drop verbose fields that bloat the dataset without adding training signal.

    We keep the message text (the agent's view), the alert, and the structured
    summaries that the agent would actually consume. We drop the dependency
    graph (constant per family), the full deployment history, and any nested
    error stack traces.
    """
    keep_keys = {"message", "done", "reward", "alert", "logs", "metrics",
                 "diagnostic_result", "error", "services_summary"}
    return {k: v for k, v in obs.items() if k in keep_keys and v not in (None, [], {})}


def episode_to_raw_rows(events: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """Convert one episode.jsonl into a list of raw step rows."""
    start = next((e for e in events if e.get("type") == "start"), {})
    end = next((e for e in reversed(events) if e.get("type") == "end"), {})
    rows: List[Dict[str, Any]] = []

    task_id = start.get("task_id", "")
    seed = start.get("seed")
    difficulty = start.get("difficulty", 0.5)
    episode_id = start.get("episode_id")
    final_score = end.get("score", 0.0)
    final_resolved = end.get("resolved", False)

    for ev in events:
        if ev.get("type") != "step":
            continue
        rows.append({
            "task_id": task_id,
            "seed": seed,
            "difficulty": difficulty,
            "episode_id": episode_id,
            "step": ev.get("step"),
            "action": ev.get("action"),
            "observation": _strip_observation(ev.get("observation", {})),
            "reward_breakdown": ev.get("reward_breakdown") or {},
            "step_message": ev.get("message", ""),
            "episode_score": final_score,
            "episode_resolved": final_resolved,
        })
    return rows


# ----------------------------------------------------------------------------
# Step 3: build SFT chat rows (random episodes + ideal trajectories)
# ----------------------------------------------------------------------------


def episode_to_sft_rows(events: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """Render an episode as {system, user, assistant, ...} chat rows.

    The assistant row carries the action that was taken at this step. We
    include all steps (not only successful ones) — for raw substrate
    consumption. Downstream consumers can filter by `episode_resolved` or
    `episode_score` when training.
    """
    start = next((e for e in events if e.get("type") == "start"), {})
    end = next((e for e in reversed(events) if e.get("type") == "end"), {})
    alert = start.get("alert", "")
    task_id = start.get("task_id", "")
    seed = start.get("seed")
    rows: List[Dict[str, Any]] = []

    for ev in events:
        if ev.get("type") != "step":
            continue
        obs = ev.get("observation") or {}
        action = ev.get("action") or {}
        step_idx = ev.get("step", 1)

        if step_idx == 1:
            user_msg = f"INCIDENT ALERT:\n{alert}\n\nWhat is your first action?"
        else:
            user_msg = obs.get("message", "") + "\n\nWhat is your next action?"

        assistant_payload = {
            "thinking": "",
            "action_type": action.get("action_type"),
            "target_service": action.get("target_service"),
            "parameters": action.get("parameters") or {},
        }
        rows.append({
            "task_id": task_id,
            "seed": seed,
            "step": step_idx,
            "system": SYSTEM_PROMPT,
            "user": user_msg,
            "assistant": json.dumps(assistant_payload, indent=2),
            "source": "random_policy",
            "episode_score": end.get("score", 0.0),
            "episode_resolved": end.get("resolved", False),
        })
    return rows


def expert_sft_rows() -> List[Dict[str, Any]]:
    """Pull the senior-SRE behavioral-clone trajectories via build_sft_dataset.

    `build_sft_dataset()` already replays IDEAL_TRAJECTORIES under multiple
    seeds and returns chat rows in the exact format we want. We tag them so
    downstream filters can keep "expert only".
    """
    rows: List[Dict[str, Any]] = []
    for r in build_sft_dataset(n_seeds_per_family=3):
        rows.append({
            **r,
            "source": "ideal_trajectory",
            "episode_score": 1.0,
            "episode_resolved": True,
        })
    return rows


# ----------------------------------------------------------------------------
# Step 4: write outputs
# ----------------------------------------------------------------------------


def write_jsonl(path: Path, rows: Iterable[Dict[str, Any]]) -> int:
    path.parent.mkdir(parents=True, exist_ok=True)
    n = 0
    with path.open("w", encoding="utf-8") as fh:
        for row in rows:
            fh.write(json.dumps(row, ensure_ascii=False) + "\n")
            n += 1
    return n


def collect_all_episodes(runs_dir: Path) -> List[List[Dict[str, Any]]]:
    """Read every episode.jsonl under runs_dir into memory."""
    episodes: List[List[Dict[str, Any]]] = []
    for run_meta in iter_runs(runs_dir):
        ep_path = runs_dir / run_meta["run_id"] / "episode.jsonl"
        events = read_episode(ep_path)
        if events:
            episodes.append(events)
    return episodes


# ----------------------------------------------------------------------------
# Step 5: optional push to HF Hub
# ----------------------------------------------------------------------------


def push_to_hub(output_dir: Path, repo_id: str, token: Optional[str]) -> None:
    """Upload the dataset folder to a HuggingFace repo.

    Soft-fails with a clear message if `huggingface_hub` isn't installed —
    the dataset is still locally usable as parquet/JSONL.
    """
    try:
        from huggingface_hub import HfApi, create_repo
    except ImportError:
        print("[push] huggingface_hub not installed — skip. "
              "Install with `pip install huggingface_hub` and re-run with --push-to-hub.",
              flush=True)
        return

    print(f"[push] creating dataset repo {repo_id} (if missing)…", flush=True)
    api = HfApi(token=token)
    create_repo(repo_id, repo_type="dataset", exist_ok=True, token=token)
    print(f"[push] uploading {output_dir}…", flush=True)
    api.upload_folder(
        repo_id=repo_id,
        folder_path=str(output_dir),
        repo_type="dataset",
        token=token,
    )
    print(f"[push] done -> https://huggingface.co/datasets/{repo_id}", flush=True)


# ----------------------------------------------------------------------------
# Entrypoint
# ----------------------------------------------------------------------------


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--seeds", type=int, default=10,
                        help="Random-policy seeds per family (default 10; 30 episodes total).")
    parser.add_argument("--runs-dir", type=Path, default=DEFAULT_RUNS_DIR,
                        help="Where intermediate episode.jsonl files are written.")
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT_DIR,
                        help="Where the final HF-ready dataset files are written.")
    parser.add_argument("--push-to-hub", type=str, default=None,
                        help="HF repo id (e.g. 'metamorphs/praetor-sre-trajectories') to push to.")
    parser.add_argument("--token", type=str, default=None,
                        help="HF API token (falls back to HF_TOKEN env var).")
    parser.add_argument("--no-clean", action="store_true",
                        help="Don't clean the runs-dir before generating.")
    parser.add_argument("--include-expert", action="store_true", default=True,
                        help="Include senior-SRE IDEAL_TRAJECTORIES as 'expert' rows.")
    parser.add_argument("--no-expert", dest="include_expert", action="store_false")
    args = parser.parse_args()

    runs_dir: Path = args.runs_dir
    output_dir: Path = args.output_dir

    # 1. Clean + generate
    if runs_dir.exists() and not args.no_clean:
        print(f"[clean] removing {runs_dir}", flush=True)
        shutil.rmtree(runs_dir)
    gen_meta = generate_episodes(args.seeds, runs_dir)

    # 2. Read all episodes
    episodes = collect_all_episodes(runs_dir)
    print(f"[read] loaded {len(episodes)} episodes", flush=True)

    # 3. Convert to raw + SFT rows
    raw_rows: List[Dict[str, Any]] = []
    sft_rows: List[Dict[str, Any]] = []
    for events in episodes:
        raw_rows.extend(episode_to_raw_rows(events))
        sft_rows.extend(episode_to_sft_rows(events))

    if args.include_expert:
        expert = expert_sft_rows()
        sft_rows.extend(expert)
        print(f"[expert] +{len(expert)} ideal-trajectory rows", flush=True)

    # 4. Write outputs
    output_dir.mkdir(parents=True, exist_ok=True)
    n_raw = write_jsonl(output_dir / "raw.jsonl", raw_rows)
    n_sft = write_jsonl(output_dir / "sft.jsonl", sft_rows)
    manifest = {
        "version": "0.1.0",
        "n_episodes": gen_meta["n_episodes"],
        "n_raw_rows": n_raw,
        "n_sft_rows": n_sft,
        "policy": "random_policy(rng_seed=42)",
        "families": list(FAMILIES),
        "seeds_per_family": args.seeds,
        "difficulty": 0.5,
        "include_expert": args.include_expert,
        "generation_seconds": gen_meta["elapsed_seconds"],
        "load_snippet": (
            "from datasets import load_dataset\n"
            "ds = load_dataset('json', data_files={"
            "'sft': 'sft.jsonl', 'raw': 'raw.jsonl'})"
        ),
    }
    (output_dir / "manifest.json").write_text(json.dumps(manifest, indent=2))

    # Copy the dataset card if present, so push_to_hub uploads it as README.md
    card_src = REPO_ROOT / "docs" / "dataset_card.md"
    if card_src.exists():
        shutil.copy(card_src, output_dir / "README.md")

    print()
    print(f"  raw rows:     {n_raw:>5} -> {output_dir / 'raw.jsonl'}")
    print(f"  sft rows:     {n_sft:>5} -> {output_dir / 'sft.jsonl'}")
    print(f"  manifest:           -> {output_dir / 'manifest.json'}")
    print()

    # 5. Optional: push to hub
    if args.push_to_hub:
        push_to_hub(output_dir, args.push_to_hub, args.token)

    return 0


if __name__ == "__main__":
    sys.exit(main())
