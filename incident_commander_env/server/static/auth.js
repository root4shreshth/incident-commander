// auth.js -- client-side auth for the IncidentCommander training UI.
//
// Google sign-in: REAL OAuth via Google Identity Services (GIS).
//   - Reads <meta name="google-client-id" content="..."> from the HTML head.
//   - To enable: create a Web Client ID at https://console.cloud.google.com/apis/credentials,
//     add the deployed origin (e.g. https://hype4raj-incident-commander-env.hf.space) as an
//     "Authorized JavaScript origin", paste the Client ID into the meta tag.
//
// Apple / GitHub: backend-required OAuth flows -- these stay in demo mode for the hackathon.
//
// Storage: per-user progress is namespaced by email -> ic_completed:<email> in localStorage.
// Stored user shape: { email, name, picture?, provider, signedInAt }

const Auth = (() => {
  const USER_KEY = 'ic_user';
  const PLACEHOLDER_CLIENT_ID = 'YOUR_GOOGLE_CLIENT_ID.apps.googleusercontent.com';

  // Demo identities for providers that can't run client-side without backend OAuth.
  const DEMO_IDENTITIES = {
    apple:  { email: 'demo.apple@asme.ai',  name: 'Apple Demo',  provider: 'Apple (demo)' },
    github: { email: 'demo.github@asme.ai', name: 'GitHub Demo', provider: 'GitHub (demo)' },
  };

  function googleClientId() {
    const meta = document.querySelector('meta[name="google-client-id"]');
    const id = meta ? (meta.getAttribute('content') || '').trim() : '';
    if (!id || id === PLACEHOLDER_CLIENT_ID) return null;
    return id;
  }

  function isGoogleConfigured() { return !!googleClientId(); }

  function currentUser() {
    const s = localStorage.getItem(USER_KEY);
    if (!s) return null;
    try { return JSON.parse(s); } catch { return null; }
  }

  function isSignedIn() { return !!currentUser(); }

  function deriveName(email) {
    const local = (email || '').split('@')[0] || 'Engineer';
    return local.replace(/[._-]+/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
  }

  function signInWithEmail(email) {
    email = (email || '').trim().toLowerCase();
    if (!email || !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
      throw new Error('Please enter a valid email address.');
    }
    const user = { email, name: deriveName(email), provider: 'Email', signedInAt: Date.now() };
    localStorage.setItem(USER_KEY, JSON.stringify(user));
    return user;
  }

  // Decode the JWT credential issued by Google Identity Services.
  // This is a signed token; we trust the email/name/picture claims since they
  // were just generated by Google's auth flow in the same browser session.
  function decodeJwt(token) {
    try {
      const payload = token.split('.')[1];
      // base64url -> base64
      const b64 = payload.replace(/-/g, '+').replace(/_/g, '/');
      const padded = b64 + '='.repeat((4 - b64.length % 4) % 4);
      const json = decodeURIComponent(
        atob(padded).split('').map(c => '%' + ('00' + c.charCodeAt(0).toString(16)).slice(-2)).join('')
      );
      return JSON.parse(json);
    } catch { return null; }
  }

  function signInWithGoogleCredential(credential) {
    const claims = decodeJwt(credential);
    if (!claims || !claims.email) throw new Error('Could not read Google account.');
    const user = {
      email: claims.email,
      name: claims.name || deriveName(claims.email),
      picture: claims.picture || null,
      provider: 'Google',
      signedInAt: Date.now(),
    };
    localStorage.setItem(USER_KEY, JSON.stringify(user));
    return user;
  }

  function signInWithProvider(providerKey) {
    const id = DEMO_IDENTITIES[providerKey];
    if (!id) throw new Error('Unknown provider');
    const user = { ...id, signedInAt: Date.now() };
    localStorage.setItem(USER_KEY, JSON.stringify(user));
    return user;
  }

  function signOut() {
    // Best-effort: also revoke the Google session so the user can pick a different account.
    try {
      if (window.google && google.accounts && google.accounts.id) {
        google.accounts.id.disableAutoSelect();
      }
    } catch {}
    localStorage.removeItem(USER_KEY);
  }

  function progressKey() {
    const u = currentUser();
    return u ? `ic_completed:${u.email}` : 'ic_completed';
  }

  function loadCompleted() {
    try { return JSON.parse(localStorage.getItem(progressKey()) || '[]'); }
    catch { return []; }
  }

  function saveCompleted(list) {
    localStorage.setItem(progressKey(), JSON.stringify(list));
  }

  // Initialize Google Identity Services. Resolves once the GIS script has loaded
  // and the client has been configured. The callback receives a credential JWT.
  // Returns false if no Client ID is configured.
  function initGoogleSignIn(onCredential) {
    const clientId = googleClientId();
    if (!clientId) return false;

    return new Promise((resolve) => {
      // Wait for the GIS script to load (it's loaded async in <head>)
      const ready = () => window.google && google.accounts && google.accounts.id;
      const start = () => {
        try {
          google.accounts.id.initialize({
            client_id: clientId,
            callback: (resp) => {
              if (resp && resp.credential) onCredential(resp.credential);
            },
            auto_select: false,
            cancel_on_tap_outside: true,
            ux_mode: 'popup',
          });
          resolve(true);
        } catch (e) {
          console.error('GIS init failed:', e);
          resolve(false);
        }
      };
      if (ready()) return start();
      let tries = 0;
      const t = setInterval(() => {
        tries++;
        if (ready()) { clearInterval(t); start(); }
        else if (tries > 50) { clearInterval(t); resolve(false); } // ~5s timeout
      }, 100);
    });
  }

  function renderGoogleButton(container, opts = {}) {
    if (!window.google || !google.accounts || !google.accounts.id) return false;
    try {
      google.accounts.id.renderButton(container, {
        type: 'standard',
        theme: opts.theme || 'filled_black',
        size: 'large',
        text: 'continue_with',
        shape: 'rectangular',
        logo_alignment: 'left',
        width: opts.width || 320,
      });
      return true;
    } catch (e) {
      console.error('GIS renderButton failed:', e);
      return false;
    }
  }

  function promptGoogle() {
    if (!window.google || !google.accounts || !google.accounts.id) return false;
    try { google.accounts.id.prompt(); return true; } catch { return false; }
  }

  return {
    currentUser, isSignedIn, signInWithEmail, signInWithProvider, signInWithGoogleCredential,
    signOut, progressKey, loadCompleted, saveCompleted,
    isGoogleConfigured, initGoogleSignIn, renderGoogleButton, promptGoogle,
  };
})();

window.Auth = Auth;
